#include <stddef.h>                     // Defines NULL
#include <stdbool.h>                    // Defines true
#include <stdlib.h>

//*********************************************************************************************************************
// vShortDelay
//
//*********************************************************************************************************************
//

void vShortDelay(unsigned long ulDelayCount)     // Delay Time (CoreTimer Ticks)
{
   while (ulDelayCount > 0)   
      ulDelayCount--;   
}

//*********************************************************************************************************************
// vLongDelay
// 
//*********************************************************************************************************************
//
void vLongDelay(unsigned long ulDelay)           // Delay Time (milliseconds at 200mhz)
{
   unsigned long                 cDelay                     = 0;
 
   for (cDelay = 0; cDelay < (ulDelay * 50004); cDelay++)
   {
      asm("nop");      
   }  
}
 
//*********************************************************************************************************************
// vDelayUs
// Microsecond delay
//*********************************************************************************************************************
//
  
void vDelayUs(unsigned long ulDelay)
{
   unsigned long                 cDelay                     = 0;

   for (cDelay = 0; cDelay < (ulDelay * 50); cDelay++)
   {
      asm("nop");      
   }  
   
}
